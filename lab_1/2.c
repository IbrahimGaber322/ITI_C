#include <stdio.h>

int main() {
    int integerNumber = 42;
    float floatNumber = 3.14159;
    char character = 'A';
    char string[] = "Hello, World!";
    
    printf("Integer: %d\n", integerNumber);
    printf("Float: %.2f\n", floatNumber);
    printf("Character: %c\n", character);
    printf("String: %s\n", string);

    return 0;
}
